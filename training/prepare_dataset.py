"""
Dataset Preparation Script
============================
Extract frames from gameplay videos and prepare them for YOLO annotation.
"""

import cv2
import argparse
import random
import shutil
from pathlib import Path
from tqdm import tqdm

import sys
sys.path.insert(0, str(Path(__file__).parent.parent))
from config import settings


def extract_frames(
    video_path: str,
    output_dir: str,
    fps: int = 5,
    max_frames: int = 1000,
    format: str = "jpg",
) -> int:
    """
    Extract frames from a video file at a specified frame rate.

    Args:
        video_path: Path to the input video file
        output_dir: Directory to save extracted frames
        fps: Target extraction frame rate
        max_frames: Maximum number of frames to extract
        format: Output image format (jpg or png)

    Returns:
        Number of frames extracted
    """
    video_path = Path(video_path)
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    if not video_path.exists():
        raise FileNotFoundError(f"Video not found: {video_path}")

    cap = cv2.VideoCapture(str(video_path))
    video_fps = cap.get(cv2.CAP_PROP_FPS)
    total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    frame_interval = max(1, int(video_fps / fps))

    print(f"Video: {video_path.name}")
    print(f"  FPS: {video_fps:.1f}, Total Frames: {total_frames}")
    print(f"  Extracting every {frame_interval}th frame (~{fps} fps)")
    print(f"  Output: {output_dir}")

    extracted = 0
    frame_idx = 0

    pbar = tqdm(total=min(max_frames, total_frames // frame_interval), desc="Extracting")

    while cap.isOpened() and extracted < max_frames:
        ret, frame = cap.read()
        if not ret:
            break

        if frame_idx % frame_interval == 0:
            filename = output_dir / f"frame_{extracted:05d}.{format}"
            cv2.imwrite(str(filename), frame)
            extracted += 1
            pbar.update(1)

        frame_idx += 1

    cap.release()
    pbar.close()
    print(f"✅ Extracted {extracted} frames to {output_dir}")
    return extracted


def create_dataset_structure(
    frames_dir: str,
    dataset_dir: str,
    train_ratio: float = 0.8,
    shuffle: bool = True,
) -> dict:
    """
    Organize extracted frames into YOLO train/val split structure.

    Args:
        frames_dir: Directory containing extracted frames
        dataset_dir: Output dataset directory
        train_ratio: Ratio of images for training (rest goes to validation)
        shuffle: Whether to shuffle images before splitting

    Returns:
        Dictionary with split statistics
    """
    frames_dir = Path(frames_dir)
    dataset_dir = Path(dataset_dir)

    # Create structure
    train_img_dir = dataset_dir / "images" / "train"
    val_img_dir = dataset_dir / "images" / "val"
    train_label_dir = dataset_dir / "labels" / "train"
    val_label_dir = dataset_dir / "labels" / "val"

    for d in [train_img_dir, val_img_dir, train_label_dir, val_label_dir]:
        d.mkdir(parents=True, exist_ok=True)

    # Collect all images
    images = sorted(list(frames_dir.glob("*.jpg")) + list(frames_dir.glob("*.png")))
    if shuffle:
        random.shuffle(images)

    split_idx = int(len(images) * train_ratio)
    train_images = images[:split_idx]
    val_images = images[split_idx:]

    print(f"Total images: {len(images)}")
    print(f"  Train: {len(train_images)}")
    print(f"  Val: {len(val_images)}")

    # Copy images
    for img in tqdm(train_images, desc="Copying train"):
        shutil.copy2(img, train_img_dir / img.name)
    for img in tqdm(val_images, desc="Copying val"):
        shutil.copy2(img, val_img_dir / img.name)

    # Create data.yaml
    data_yaml = dataset_dir / "data.yaml"
    yaml_content = f"""# FPS Aim Analyzer - YOLO Dataset Configuration
# Auto-generated by prepare_dataset.py

path: {dataset_dir.resolve()}
train: images/train
val: images/val

names:
  0: crosshair
  1: enemy_head
  2: enemy_body
  3: target

# Annotation Instructions:
# Use Roboflow, CVAT, or LabelImg to annotate images
# Export in YOLO format (.txt files with: class_id x_center y_center width height)
# Place label files in labels/train and labels/val directories
"""
    with open(data_yaml, "w") as f:
        f.write(yaml_content)

    stats = {
        "total": len(images),
        "train": len(train_images),
        "val": len(val_images),
        "train_ratio": train_ratio,
        "data_yaml": str(data_yaml),
    }

    print(f"✅ Dataset structure created at {dataset_dir}")
    print(f"📄 data.yaml saved at {data_yaml}")
    return stats


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Prepare YOLO dataset from gameplay videos")
    subparsers = parser.add_subparsers(dest="command")

    # Extract command
    extract_parser = subparsers.add_parser("extract", help="Extract frames from video")
    extract_parser.add_argument("video", help="Path to video file")
    extract_parser.add_argument("--output", "-o", default="data/raw/frames", help="Output directory")
    extract_parser.add_argument("--fps", type=int, default=5, help="Extraction FPS")
    extract_parser.add_argument("--max-frames", type=int, default=1000, help="Max frames to extract")
    extract_parser.add_argument("--format", choices=["jpg", "png"], default="jpg")

    # Split command
    split_parser = subparsers.add_parser("split", help="Create train/val split")
    split_parser.add_argument("frames_dir", help="Directory with extracted frames")
    split_parser.add_argument("--output", "-o", default="dataset", help="Output dataset directory")
    split_parser.add_argument("--train-ratio", type=float, default=0.8, help="Training split ratio")

    args = parser.parse_args()

    if args.command == "extract":
        extract_frames(args.video, args.output, args.fps, args.max_frames, args.format)
    elif args.command == "split":
        create_dataset_structure(args.frames_dir, args.output, args.train_ratio)
    else:
        parser.print_help()
